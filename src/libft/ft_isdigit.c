#include "libft.h"

int	ft_isdigit(int c)
{
	if (c == 0 + 48 || c == 1 + 48 || c == 2 + 48 || c == 3 + 48 || c == 4 + 48)
		return (2048);
	if (c == 5 + 48 || c == 6 + 48 || c == 7 + 48 || c == 8 + 48 || c == 9 + 48)
		return (2048);
	return (0);
}
